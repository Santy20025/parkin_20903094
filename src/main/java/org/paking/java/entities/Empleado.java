package org.paking.java.entities;

public class Empleado {
    public String nombre;
    public String apellido;
    public String codigo;
    
    public Empleado() {
    }

    public Empleado(String nombre, String apellido, String codigo) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.codigo = codigo;
    } 

}
